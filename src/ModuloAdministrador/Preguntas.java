    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        String codigo = txtID.getText();
        String Usuario = txtPregunta.getText();
        String Contrasena = txtDescripcion.getText();
        //String telefono = txtTelefono.getText();

        if(isBlank(codigo)){
            JOptionPane.showMessageDialog(null, "Seleccione la fila para actualizar");
        }else{
            if(isBlank(Usuario) || isBlank(Contrasena) ){

            }else{
                pregunta.setPregunta(Usuario);
                pregunta.setDescripcion(Contrasena);
                //pregunta.setDireccion_proveedor(direccion);
                pregunta.setCodigo(Integer.parseInt(codigo));
                mpregunta.ModificarPreguntas(pregunta);

                LimpiarTabla();
                ListarPreguntas();
                LimpiarCampos();
                JOptionPane.showMessageDialog(null, "Datos actualizados correctamente");
                Deshabilitar();
                btnGuardar.setEnabled(true);
                btnNuevo.setEnabled(true);
                btnActualizar.setEnabled(true);
                btnEliminar.setEnabled(true);

            }
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String codigo = txtID.getText();

        if(isBlank(codigo)){
            JOptionPane.showMessageDialog(null, "Seleccione la fila a eliminar");
        }else{
            int confirmacion = JOptionPane.showConfirmDialog(null, "Esta seguro de eliminar");
            if(confirmacion == 0){
                int id = Integer.parseInt(txtID.getText());
                mpregunta.EliminarPreguntas(id);
                LimpiarTabla();
                LimpiarCampos();
                ListarPreguntas();
                Deshabilitar();
                btnGuardar.setEnabled(true);
                btnNuevo.setEnabled(true);
                btnActualizar.setEnabled(true);
                btnEliminar.setEnabled(true);

            }
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

