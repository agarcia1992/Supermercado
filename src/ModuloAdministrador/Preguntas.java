    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        String Usuario = txtPregunta.getText();
        String Contrasena = txtDescripcion.getText();

        if(isBlank(Usuario) || isBlank(Contrasena) ){
            JOptionPane.showMessageDialog(null,"Los campos estan vacios");
            Deshabilitar();
        }else{
            pregunta.setPregunta(Usuario);
            pregunta.setDescripcion(Contrasena);
            pregunta.setEstado("activo"); 
            mpregunta.RegistrarPreguntas(pregunta);
            JOptionPane.showMessageDialog(null, "Datos ingresados correctamente");
            LimpiarTabla();
            LimpiarCampos();
            Deshabilitar();
            btnActualizar.setEnabled(true);
            btnEliminar.setEnabled(true);
            ListarPreguntas();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

