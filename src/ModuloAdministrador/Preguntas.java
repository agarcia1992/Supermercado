    public void ListarPreguntas(){
     List<Pregunta> Listarpregunta = mpregunta.ListarPreguntas();
        tabla = (DefaultTableModel) Tabla.getModel();
        Object[] ob = new Object[4];
        for (int i = 0; i < Listarpregunta.size(); i++) {
            ob[0] = Listarpregunta.get(i).getCodigo();
            ob[1] = Listarpregunta.get(i).getPregunta();
            ob[2] = Listarpregunta.get(i).getDescripcion();
            ob[3] = Listarpregunta.get(i).getEstado(); 
            tabla.addRow(ob);
        }
        Tabla.setModel(tabla);
    }
    
