    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        String Nombre = txtNombre.getText();
        String Telefono = txtTelefono.getText();
        String Correo = txtCorreo.getText();
        String Direccion = txtDireccion.getText();
        String Mision = txtMision.getText();
        String Vision = txtVision.getText();
        String Ciudad = idCiudad.getText();

        if(isBlank(Nombre) || isBlank(Telefono) || isBlank(Direccion)|| isBlank(Correo) || isBlank(Mision) || isBlank(Vision)){
            JOptionPane.showMessageDialog(null,"Los campos estan vacios");
            Deshabilitar();
        }else{
            prov.setNombre(Nombre);
            prov.setTelefono(Integer.parseInt(Telefono));
            prov.setCorreo(Correo);
            prov.setDireccion(Direccion);
            prov.setMision(Mision);
            prov.setVision(Vision);
            prov.setCodigo_ciudad(Integer.parseInt(Ciudad));
            mprov.RegistrarEmpresa(prov);
            JOptionPane.showMessageDialog(null, "Datos ingresados correctamente");
            LimpiarTabla();
            LimpiarCampos();
            Deshabilitar();
            btnActualizar.setEnabled(true);
            btnEliminar.setEnabled(true);
            ListarEmpresas();
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

