    public void ListarEmpresas(){
     List<Empresa> ListarPr = mprov.ListarEmpresa();
        tablapro = (DefaultTableModel) TablaEmpresas.getModel();
        Object[] ob = new Object[9];
        for (int i = 0; i < ListarPr.size(); i++) {
            ob[0] = ListarPr.get(i).getCodigo_empresa();
            ob[1] = ListarPr.get(i).getNombre();
            ob[2] = ListarPr.get(i).getTelefono();
            ob[3] = ListarPr.get(i).getCorreo();
            ob[4] = ListarPr.get(i).getDireccion();
            ob[5] = ListarPr.get(i).getMision();
            ob[6] = ListarPr.get(i).getVision();
            ob[7] = ListarPr.get(i).getCodigo_ciudad();
            ob[8] = ListarPr.get(i).getEstado();
            tablapro.addRow(ob);
        }
        TablaEmpresas.setModel(tablapro);
    }
