    private void cboCiudadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboCiudadActionPerformed

        idCiudad.setText("");
        String itemSeleecionado = (String)cboCiudad.getSelectedItem();
        String sql = "SELECT codigo_ciudad  FROM ciudad WHERE nombre_ciudad = '"+itemSeleecionado+"'";

        try {
            con = cn.getConnection2();
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            while(rs.next()){

                idEmp = rs.getString("codigo_ciudad");

            }
            idCiudad.setText(idEmp);

        } catch (SQLException e) {
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_cboCiudadActionPerformed

